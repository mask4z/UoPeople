package CS1103.test;

/**
 * This class is just to test the switch statement and how it can be used.
 */
public class Switch {

    public static void main(String[] args) {
        Integer a = 10;

        switch (a) {
            case 1:
                System.out.println("The number is 1");
                break;
            case 2:
                System.out.println("The number is 2");
                break;
            case 5:
                System.out.println("The number is 5");
                break;
            case 10:
                System.out.println("The number is 10");
                break;
        }
    }

}
